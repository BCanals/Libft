#include <stddef.h>

char	*ft_strrchr(const char *s, int c)
{
	const char *p;
	
	p = s;
	while (*s)
		s++;
	while (s >= p)
	{
		if (*s == c)
			return (char *)(s);
		s--;
	}
	return (NULL);
}
